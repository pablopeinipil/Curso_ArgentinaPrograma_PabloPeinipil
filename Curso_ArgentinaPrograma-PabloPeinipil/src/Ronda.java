public class Ronda {


    private String NumRonda;
    private int NumPartidos;



    // GETTER Y SETTER


    public String getNumRonda() {
        return NumRonda;
    }

    public void setNumRonda(String numRonda) {
        NumRonda = numRonda;
    }

    public int getNumPartidos() {
        return NumPartidos;
    }

    public void setNumPartidos(int numPartidos) {
        NumPartidos = numPartidos;
    }



}
